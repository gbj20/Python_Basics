'''
Title, capitalized, upper, lower, swapcase,
isupper, islower, isalpha, isalnum,isspace
'''

a = "gayatri jadhav"

print(a.title())

print(a.capitalize())

print(a.upper())

print(a.lower())

# convert lowercase character into uppercase and vise verse.
print(a.swapcase())

print(a.isupper())

print(a.islower())

print(a.isalpha())

print(a.isalnum())

print(a.isspace())

print(a.isdigit())
